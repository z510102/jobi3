//=============================================================================
//コンストラクタ
//=============================================================================
CBg::CBg(int nPriority) : CScene2D(nPriority)
{
	m_nPriority = nPriority;
}

//=============================================================================
//デストラクタ
//=============================================================================
CBg::~CBg()
{
}

//=============================================================================
//初期化処理
//=============================================================================
HRESULT CBg::Init(void)
{
	CScene2D::Init();
	return S_OK;
}

//=============================================================================
//終了処理
//=============================================================================
void CBg::Uninit(void)
{
	CScene2D::Uninit();
	//オブジェクトの破棄
	Release();
}

//=============================================================================
//更新処理
//=============================================================================
void CBg::Update(void)
{

}

//=============================================================================
//描画処理
//=============================================================================
void CBg::Draw(void)
{
	CScene2D::Draw();
}

//=============================================================================
//背景の設定
//=============================================================================
CBg * CBg::Create(D3DXVECTOR3 pos, D3DXVECTOR3 size, int nType)
{
	// 背景のインスタンス生成
	CBg *pBg = new CBg(PRIORITY_BG);

	// 背景の準備
	if (pBg != NULL)
	{
		pBg->Init();
		pBg->SetPosition(pos, size);
		pBg->BindTexture(CManager::GetTexture()->SetTextureType(nType));
	}
	return pBg;
}
